package Assign6;
//Q2:  W.A.J.P in Java to display the first 10 natural numbers using while loop.
public class Natural10Numbers2 {
	public static void main(String[] args) {
		System.out.println("First 10 natural numbers are: ");
		int i =1;
		while (i<=10) {
			System.out.println(i);
			i++;
		}
	}

}
